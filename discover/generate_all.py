# ─── STEP 4: Complexity Assessment ────────────────────────────────────────────
def compute_complexity(app, tech_result):
    aid = app["app_id"]
    servers = len(app.get("server_instances", [])) if isinstance(app.get("server_instances"), list) else 1
    interfaces = app.get("external_interface_count", 0)
    env_count = app.get("environment_count", 1)
    criticality = app.get("business_criticality", "Medium")
    containerized = app.get("is_containerized", "No")
    deployment = app.get("deployment_type", "")
    ci_cd = app.get("ci_cd_present", "No")
    arch = app.get("application_architecture", "")

    components = tech_result["components_analyzed"]
    eol_count = sum(1 for c in components if c["component_status"] == "EOL")
    outdated_count = sum(1 for c in components if c["component_status"] == "OUTDATED")

    # Technology age score (25%)
    if eol_count >= 3:
        tech_score = 9
    elif eol_count == 2:
        tech_score = 8
    elif eol_count == 1:
        tech_score = 7
    elif outdated_count >= 3:
        tech_score = 6
    elif outdated_count >= 2:
        tech_score = 5
    elif outdated_count == 1:
        tech_score = 4
    else:
        tech_score = 2

    # Integration complexity (20%)
    if interfaces >= 20:
        integ_score = 9
    elif interfaces >= 15:
        integ_score = 8
    elif interfaces >= 10:
        integ_score = 7
    elif interfaces >= 7:
        integ_score = 6
    elif interfaces >= 5:
        integ_score = 5
    elif interfaces >= 3:
        integ_score = 4
    elif interfaces >= 1:
        integ_score = 3
    else:
        integ_score = 1

    # Infrastructure (15%)
    infra_raw = servers + env_count
    if infra_raw >= 6:
        infra_score = 8
    elif infra_raw >= 4:
        infra_score = 6
    elif infra_raw >= 3:
        infra_score = 4
    else:
        infra_score = 2

    # Business criticality (15%)
    crit_map = {"Critical": 9, "High": 7, "Medium": 5, "Low": 3}
    crit_score = crit_map.get(criticality, 5)

    # Architecture/code complexity (15%)
    arch_score = 5
    lang = app.get("programming_language", "")
    if lang in ("COBOL-2014", "FORTRAN 2018", "Perl"):
        arch_score = 8
    elif arch == "1-Tier":
        arch_score = 7
    elif containerized == "No" and arch == "3-Tier":
        arch_score = 5
    elif containerized == "Yes":
        arch_score = 3

    # Data complexity (10%)
    db = app.get("database_engine", "")
    if "Oracle" in db or "DB2" in db:
        data_score = 8
    elif "SQL Server" in db:
        data_score = 5
    elif any(x in db for x in ["PostgreSQL", "MySQL", "MongoDB", "Aurora"]):
        data_score = 4
    else:
        data_score = 5

    final = (tech_score * 0.25 + integ_score * 0.20 + infra_score * 0.15 +
             crit_score * 0.15 + arch_score * 0.15 + data_score * 0.10)
    score = min(10, max(1, round(final)))

    if score <= 3:
        classification = "LOW"
    elif score <= 6:
        classification = "MEDIUM"
    else:
        classification = "HIGH"

    reasoning = (f"Technology age score {tech_score} ({eol_count} EOL, {outdated_count} outdated components). "
                 f"Integration score {integ_score} ({interfaces} external interfaces). "
                 f"Infrastructure score {infra_score} ({servers} servers, {env_count} environments). "
                 f"Criticality score {crit_score} ({criticality}). "
                 f"Architecture score {arch_score}. Data score {data_score}. "
                 f"Weighted final: {final:.1f} → {score} ({classification}).")

    return {
        "_comment": COMMENT,
        "application_identifier": aid,
        "complexity_score": score,
        "complexity_class": classification,
        "confidence": 8,
        "reasoning": reasoning,
        "contributing_factors": {
            "number_of_servers": servers,
            "number_of_databases": 1,
            "number_of_environments": env_count,
            "number_of_interfaces": interfaces,
            "business_criticality": criticality,
            "number_of_outdated_technologies": outdated_count,
            "number_of_eol_technologies": eol_count,
            "number_of_dependencies": 0,
            "ci_cd_present": ci_cd,
            "containerized": containerized
        }
    }

