    solution = app.get("solution_type", "")
    arch = app.get("application_architecture", "")
    criticality = app.get("business_criticality", "")

    components = tech_result["components_analyzed"]
    os_comp = next((c for c in components if c["component_type"] == "os"), None)
    server_comp = next((c for c in components if c["component_type"] == "application_server"), None)
    db_comp = next((c for c in components if c["component_type"] == "database"), None)
    lang_comp = next((c for c in components if c["component_type"] == "programming_language"), None)

