        f"| **Deployment** | {app.get('deployment_type','N/A')} |",
        f"| **Containerized** | {app.get('is_containerized','N/A')} |",
        f"| **Architecture** | {app.get('application_architecture','N/A')} |",
        f"| **Business Unit** | {app.get('business_unit','N/A')} |",
        f"| **External Interfaces** | {app.get('external_interface_count',0)} |",
