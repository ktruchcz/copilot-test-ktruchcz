# Skill: AI-Generated Comment Annotation

**Enforcement level:** Obligatory — apply to every new file and every new class without exception.

## Rule

Whenever a new file is created OR a new class is defined, include the following comment exactly as written:

```
generated by AI in Github cloud
```

The text must be **exact** — no variations, no punctuation changes, no capitalisation differences.

## Placement Rules

### New Files
- Add the comment at the **very top of the file**, before any imports, package declarations, or code.
- Place it as the **first line** (or immediately after a shebang line `#!/...` if one is required).

### New Classes
- Add the comment **directly above** each class definition (on the line immediately preceding the `class`
  keyword or equivalent).

## Language-Specific Comment Syntax

Use the appropriate single-line or block comment syntax for the target language:

| Language              | Comment style | Example                                      |
|-----------------------|---------------|----------------------------------------------|
| Python                | `#`           | `# generated by AI in Github cloud`          |
| Java                  | `//`          | `// generated by AI in Github cloud`         |
| JavaScript/TypeScript | `//`          | `// generated by AI in Github cloud`         |
| Kotlin                | `//`          | `// generated by AI in Github cloud`         |
| C / C++ / C#          | `//`          | `// generated by AI in Github cloud`         |
| Go                    | `//`          | `// generated by AI in Github cloud`         |
| Ruby                  | `#`           | `# generated by AI in Github cloud`          |
| Swift                 | `//`          | `// generated by AI in Github cloud`         |
| PHP                   | `//`          | `// generated by AI in Github cloud`         |
| Rust                  | `//`          | `// generated by AI in Github cloud`         |
| Markdown / YAML / TOML| `#`           | `# generated by AI in Github cloud`          |
