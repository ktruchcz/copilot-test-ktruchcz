#!/usr/bin/env bash
# =============================================================================
# setup-obligatory-skills.sh
#
# Creates all obligatory skill directories and SKILL.md files derived from
# the cookbooks in cookbooks/. Run this script once to materialise the skills
# so the transformation.migration agent can load them automatically via the
# glob pattern `obligatory_*`.
#
# Usage:
#   bash .specify/scripts/bash/setup-obligatory-skills.sh
#
# Safe to re-run: existing files are NOT overwritten (exits gracefully).
# =============================================================================

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../../.." && pwd)"
SKILLS_DIR="$REPO_ROOT/.github/skills"

echo "📂 Repository root : $REPO_ROOT"
echo "📂 Skills directory: $SKILLS_DIR"
echo ""

# ---------------------------------------------------------------------------
# Helper: write a skill file, skipping if it already exists
# ---------------------------------------------------------------------------
write_skill() {
    local skill_dir="$1"
    local content="$2"

    mkdir -p "$skill_dir"

    local skill_file="$skill_dir/SKILL.md"
    if [ -f "$skill_file" ]; then
        echo "  ⏭  Skipped (already exists): $skill_file"
    else
        printf '%s\n' "$content" > "$skill_file"
        echo "  ✅ Created: $skill_file"
    fi
}

